status_keywords = ['accept', 'answer', 'compilation', 'compile', 'error',
    'exceed', 'limit', 'memory', 'runtime', 'time', 'wrong']

# Fetch status every 3 seconds
# Should run as daemon
def status_daemon():
    while True:
        try:
            raw_status = requests.get(status_url, timeout=5).text.encode("utf-8")
            raw_status = json.loads(raw_status)['data']

            refined_status = {}
            for status in raw_status:
                sid = status[0]
                result = status[3]
                refined_status[sid] = result

            submissions = Submission.objects.filter(status__in=[Submission.WAIT, Submission.JUDGING])

            for submission in [s for s in submissions if s.problem.judge_source == Problem.OTHER and s.other_judge_sid]:
                status = refined_status.get(submission.other_judge_sid)
                if status:
                    status_lower = status.lower()
                    # If any of the keyword is in the status, write result to submission
                    for keyword in status_keywords:
                        if keyword in status_lower:
                            submission.status = status
                submission.save()

            time.sleep(3)
        except:
            logger.error('Error in fetching vjudge status')

